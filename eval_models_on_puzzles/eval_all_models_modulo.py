"""
This program tests a variety of LLMs using Pass@K attempts and the LLM-Modulo framework (LLM + external verifier) on chess puzzles.
It limits responses to 20 tokens for each attempt and tries to parse a UCI/SAN-formatted move generated by a LLM. 

For Pass@K, K responses are generated by the LLM and the LLM response is treated as correct if any
of its responses are a valid solution to the puzzle.

For LLM Modulo, LLM responses are gated by 2 critics:
1. Sanity (legal move) checker - Ensures the move generated by the LLM is a valid move in the position
2. Accuracy (good move) checker - Ensures the move generated by the LLM improves the evaluation of the position.
LLMs are given up to K tries, with each incorrect response being appended with feedback from the corresponding critic
and backprompted to the LLM.

Both methods use K=10 and temperature=0.7 sampling for non-deterministic generation.

Sanity = 1 - (proportion of parse failures) = percent of moves generated that are legal

A move is treated as correct if it matches the best move recorded OR
yields a mate score equivalent to the best move (decreases the mate-in-X evaluation by 1).
ex) Mate in 2 -> Mate in 1

Please refer to puzzle_utils.py for documentation on the functions:
sample_puzzles() - handles puzzle sampling 
get_engine() - creates custom instances of Stockfish
check_position_accuracy() - checks if the response by a LLM/chess engine is the best move in a position
                            (including alternative solutions!)
"""

