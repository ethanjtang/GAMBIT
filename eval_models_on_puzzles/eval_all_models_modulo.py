'''
This helper function evaluates a LLM using the LLM-Modulo loop on puzzle positions.
Two HARD critics gate each LLM response before accepting it as the predicted move:
  1. Move validity (Critic 1): the move must be legal in the position. If not, the LLM is
     reprompted with feedback containing a list of all legal moves.
  2. Eval improvement (Critic 2): the move must reduce the current position's evaluation
     (which ends up being mate-in-N -> mate-in-[N-1] for checkmate puzzles).
     If not, the LLM is reprompted with feedback about the current eval of the puzzle position
     and the eval of their played move (which does not improve the current eval).

LLMs are allowed up to max_tries attempts to provide a legal move that improves the evaluation of the position.
Feedback from past attempts is appended to the original prompt during reprompting
(so the LLM can "see" what previous attempts it did and why they were wrong).
The reprompting loop is reset after 3 consecutive invalid parses to avoid blowing up the prompt.
(preliminary tests showed the LLM would start hallucinating badly as the prompt grew)

label            - LLM model label
model            - LLM (duh)
tokenizer        - LLM tokenizer
device           - where the LLM is
puzzles_by_theme - sample of N puzzles for X themes
judge_engine     - SF18 at depth=20, used for eval improvement checks
max_tries        - max total attempts per position (default MODULO_MAX_TRIES)
'''
def evaluate_llm_modulo_pass(label: str, model, tokenizer, device: str,
                              puzzles_by_theme: dict, judge_engine: chess.engine.SimpleEngine,
                              max_tries: int = MODULO_MAX_TRIES):

    print(f'\n{"=" * 70}')
    print(f'MODEL: {label} [LLM-Modulo, max_tries={max_tries}, temperature={TEMPERATURE}]')
    print(f'{"=" * 70}')

    # results
    total_positions_correct = 0
    total_positions = 0
    total_puzzles_solved = 0
    total_puzzles = 0
    total_parse_failed = 0  # positions where no valid move (according to Critics 1 and 2) was ever produced

    # For each puzzle theme...
    for theme, (puzzles, _) in puzzles_by_theme.items():

        print(f'\n  Theme: {theme} ({len(puzzles)} puzzles)')
        print(f'  {"-" * 60}')

        # Theme-wide results
        theme_positions_correct = 0
        theme_positions_total = 0
        theme_puzzles_solved = 0
        theme_parse_failed = 0

        # For each puzzle...
        for puzzle_idx, puzzle in enumerate(puzzles):

            # Puzzle-wide results
            puzzle_positions_correct = 0
            print(f'\n  Puzzle {puzzle_idx + 1}/{len(puzzles)}:')

            # For each puzzle position...
            for pos_idx, (fen, best_uci, _) in enumerate(puzzle):

                # Build initial prompt which grows with each failed attempt
                initial_prompt = build_prompt(fen)
                current_prompt = initial_prompt

                # Fixed board with original puzzle position
                board = chess.Board(fen)

                # Precompute the eval of og position for Critic 2 feedback
                og_score = judge_engine.analyse(
                    board, chess.engine.Limit(depth=SF18_DEPTH, time=SF18_TIMEOUT)
                ).get("score")
                og_mate = og_score.relative.mate() if og_score and og_score.relative.is_mate() else None
                og_eval = format_mate_eval(og_mate) # formatted as "Mate in {mate_n} (for you)"

                # Precompute list of legal moves in og position
                legal_moves_str = ', '.join(m.uci() for m in board.legal_moves)

                # Critic 1 pass -> last valid move seen
                # Critic 1 fail -> None
                predicted_uci = None
                correct = False
                # List to log LLM attempts
                attempt_logs = []  # (attempt_num, raw_response, parsed_uci, critic_result)

                # save the first parsed move to avoid incorrectly triggering a parse failure
                best_valid_move = None
                # counts consecutive parse failures (to reset LLM when it is stuck)
                consecutive_parse_failures = 0
                # use to check if the model repeats itself (to avoid reprompting with duplicate feedback)
                last_parse_target = None

                # Try up to K (max_tries) times to generate a response which is accepted by Critics 1 and 2
                for attempt in range(max_tries):

                    # debugging printout
                    # print(f'\n    Position {pos_idx + 1}, attempt {attempt + 1} prompt:\n{current_prompt}')

                    # Generate model response
                    raw_response = generate_response(model, tokenizer, current_prompt, device, temperature=TEMPERATURE)
                    
                    # debugging printout
                    # print(f"    Model: {raw_response}")

                    # Parse and append (for reprompting) first line of model output
                    # Addresses 2 headaches:
                    # 1. Context window increasing without any meaningful change in model output
                    # 2. Model echoes full list of valid UCI moves from Critic 1 feedback which is incorrectly parsed
                    first_line = raw_response.split('\n')[0].strip()
                    parse_target = first_line if first_line else raw_response

                    # Strip leading Model: and User: prefixes echoed in model response
                    # Prevents recursive blowup like:
                    # "Model: 1.Nxd5" → "Model: Model: 1.Nxd5" → "Model: Model: Model: 1.Nxd5" ...
                    # as the model is continously reprompted
                    while parse_target.startswith('Model:'):
                        parse_target = parse_target[len('Model:'):].strip()
                    while parse_target.startswith('User:'):
                        parse_target = parse_target[len('User:'):].strip()

                    # Parse a move from model response and print
                    parsed = parse_move(parse_target, board)
                    # print(f"    Parsed: {parsed!r}")

                    # --- Critic #1: move validity ---

                    # Critic #1 fail: Model did not output a valid move in the position
                    if parsed is None:
                        consecutive_parse_failures += 1
                        # Check if the model responses have yielded 3 consecutive parse failures
                        # Note that parse failures = invalid moves
                        if consecutive_parse_failures >= 3:
                            # Reset context after 3 consecutive parse failures
                            # Since there is no point in repeating the same feedback from Critic #1 or #2 multiple times
                            # And k=0.7 gives nondeterministic generation
                            # So the model has a better shot with multiple sub-tries per K=10 Modulo loop
                            print(f"    [Context reset after {consecutive_parse_failures} consecutive parse failures]")
                            current_prompt = initial_prompt
                            consecutive_parse_failures = 0
                            last_parse_target = None
                        # Check if the model output the same move as the previous prompt
                        elif parse_target != last_parse_target:
                            # Add feedback only if the model is trying a different move
                            # This prevents the model from blowing up the prompt with identical feedback strings
                            current_prompt += (
                                f"\nModel: {parse_target}\n"
                                "User: The move you provided is invalid. Please provide one of the following legal moves in the position:\n"
                                f"{legal_moves_str}\n"
                                "Best move:"
                            )
                            last_parse_target = parse_target
                        attempt_logs.append((attempt + 1, raw_response, None, 'INVALID'))
                        continue

                    # Critic #1 pass: Model provides valid (legal) move in the position
                    # reset failure counter and record first legal move seen
                    consecutive_parse_failures = 0
                    if best_valid_move is None:
                        best_valid_move = parsed  # fallback if model output never passes Critic #2

                    # --- Critic 2: eval improvement ---

                    # Get the board state and eval after the provided model move (used for both the check and feedback)
                    board_after = chess.Board(fen)
                    board_after.push(chess.Move.from_uci(parsed))
                    new_score = judge_engine.analyse(
                        board_after, chess.engine.Limit(depth=SF18_DEPTH, time=SF18_TIMEOUT)
                    ).get("score")

                    # new_score is from the opponent's perspective, convert to general mate score
                    new_mate_opp = new_score.relative.mate() if new_score and new_score.relative.is_mate() else None
                    
                    # Critic #2 pass: Model's move is valid and DOES improve the evaluation of the position

                    # A correct move reduces mate-in-N to mate-in-(N-1): opponent now faces -(N-1) from their view
                    if og_mate is not None and new_mate_opp == -(og_mate - 1):
                        # yippee!!!
                        predicted_uci = parsed
                        correct = True
                        attempt_logs.append((attempt + 1, raw_response, parsed, 'CORRECT'))
                        break

                    # Critic #2 fail: Model's move is valid but DOESN'T improve the evaluation of the position

                    # Flip new_score to current side's perspective for the feedback message.
                    new_mate_us = -new_mate_opp if new_mate_opp is not None else None

                    # Model move lost the forced mate eval
                    if new_mate_us is None:
                        new_eval_feedback = 'no forced mate for you'
                    # Model move gives forced mate for original side to move
                    elif new_mate_us > 0:
                        new_eval_feedback = f'Mate in {new_mate_us} for you'
                    # DISASTER! Model move gives forced mate for opponent
                    else:
                        new_eval_feedback = f'Mate in {new_mate_us} for your opponent'

                    # Only append feedback to model reprompt if it provided a different move from the last attempt
                    # Same reasoning as similar gate for Critic #1, adding additional identical feedback messages
                    # for model output is not productive and blows up context window
                    # making it less interpretable for humans and not really helping the model in any way
                    # from initial very small tests, the models seem to hallucinate more the longer the prompt gets
                    if parse_target != last_parse_target:
                        # get list of all legal moves without model response
                        other_legal_moves_str = ', '.join(m.uci() for m in board.legal_moves if m.uci() != parsed)
                        # ask the model to choose from the filtered list of legal moves AND
                        # provide feedback on how the model's chosen move changes the positions eval
                        # The hope is that models use this as context to pick a different move:
                        #     If their current move still gives a good eval, they may choose a similar alternative.
                        #     If their current gives a bad eval, they may choose a "different-looking" move.
                        current_prompt += (
                            f"\nModel: {parse_target}\n"
                            f"User: The move you provided ({parsed}) is valid but does not improve the evaluation of the position. "
                            f"The current position is {og_eval} and the move you provided gives a position with the evaluation of {new_eval_feedback}. "
                            f"Please try one of the following alternative legal moves instead:\n"
                            f"{other_legal_moves_str}\n"
                            "Best move:"
                        )
                    # save last-parsed move for duplicate comparison
                    last_parse_target = parse_target
                    predicted_uci = parsed  # save the last valid move seen
                    attempt_logs.append((attempt + 1, raw_response, parsed, 'VALID_BUT_WRONG'))

                # fall back to first legal move seen (passed by Critic #1) if model never passes Critic #2
                if predicted_uci is None and best_valid_move is not None:
                    predicted_uci = best_valid_move

                # If no valid move was ever produced across all attempts (all moves failed by Critic #1), count as a parse failure
                if predicted_uci is None:
                    theme_parse_failed += 1
                    total_parse_failed += 1
                
                # Print per-position results
                print(f'    Position {pos_idx + 1} ({len(attempt_logs)} attempt(s)):')
                print(f'      FEN:           {fen}')
                print(f'      Best move UCI: {best_uci}')
                for (attempt_n, raw, parsed_uci, critic_result) in attempt_logs:
                    print(f'      Attempt {attempt_n}: raw={raw!r} parsed={parsed_uci!r} [{critic_result}]')
                final_status = 'CORRECT' if correct else ('WRONG' if predicted_uci else 'PARSE_FAILED')
                print(f'      Final:         {predicted_uci!r} [{final_status}]')

                # Update results for correct/incorrect predictions
                if correct:
                    puzzle_positions_correct += 1
                    theme_positions_correct += 1
                    total_positions_correct += 1
                theme_positions_total += 1
                total_positions += 1

            # Calculate puzzle-wide accuracy
            puzzle_solved = puzzle_positions_correct == len(puzzle)
            if puzzle_solved:
                theme_puzzles_solved += 1
                total_puzzles_solved += 1
            total_puzzles += 1

            puzzle_status = 'SOLVED' if puzzle_solved else 'FAILED'
            print(f'    Puzzle result: [{puzzle_status}] ({puzzle_positions_correct}/{len(puzzle)} positions correct)')

        # Calculate theme-wide accuracy
        theme_pos_acc = theme_positions_correct / theme_positions_total * 100 if theme_positions_total > 0 else 0
        theme_puzzle_acc = theme_puzzles_solved / len(puzzles) * 100 if puzzles else 0
        print(f'\n  {theme} summary:')
        print(f'    Puzzles solved:    {theme_puzzles_solved}/{len(puzzles)} ({theme_puzzle_acc:.1f}%)')
        print(f'    Positions correct: {theme_positions_correct}/{theme_positions_total} ({theme_pos_acc:.1f}%)')
        print(f'    Parse failures:    {theme_parse_failed}/{theme_positions_total}')

    # Calculate overall accuracy (across all puzzles for all themes)
    overall_pos_acc = total_positions_correct / total_positions * 100 if total_positions > 0 else 0
    overall_puzzle_acc = total_puzzles_solved / total_puzzles * 100 if total_puzzles > 0 else 0
    print(f'\n  OVERALL ({label}):')
    print(f'    Puzzles solved:    {total_puzzles_solved}/{total_puzzles} ({overall_puzzle_acc:.1f}%)')
    print(f'    Positions correct: {total_positions_correct}/{total_positions} ({overall_pos_acc:.1f}%)')
    print(f'    Parse failures:    {total_parse_failed}/{total_positions}')

    return (total_puzzles_solved, total_puzzles, total_positions_correct, total_positions, total_parse_failed)


'''
This helper function loads and evaluates a LLM using the LLM-Modulo loop on a sample of N puzzles from X themes.
Mirrors evaluate_llm_model() but only uses normal prompts.
(Since LLM-Modulo can be seen as sort of an "advanced" form of cheating prompt).
'''
def evaluate_llm_modulo_model(config: dict, puzzles_by_theme: dict, judge_engine: chess.engine.SimpleEngine):

    # model name and path
    name = config['name']
    path = config['path']

    print(f'\n{"#" * 70}')
    print(f'Loading LLM (Modulo): {name} ({path})')
    print(f'{"#" * 70}')

    # Set tokenizer based on LLM category
    if config['llama']:
        tokenizer = LlamaTokenizer.from_pretrained(path, use_fast=False)
        model = LlamaForCausalLM.from_pretrained(
            path, torch_dtype=torch.float16, device_map='auto',
            offload_folder=OFFLOAD_DIR
        )
    else:
        tokenizer = AutoTokenizer.from_pretrained(path)
        model = AutoModelForCausalLM.from_pretrained(
            path, torch_dtype=torch.float16, device_map='auto',
            offload_folder=OFFLOAD_DIR
        )

    # For LLaMa-type models
    if tokenizer.pad_token_id is None:
        tokenizer.pad_token_id = tokenizer.eos_token_id

    # Load the LLM
    model.eval()
    device = 'cuda'
    print('Model loaded.\n')

    # Evaluate LLM using LLM-Modulo pass on N puzzles from X themes
    label = f'{name} (modulo)'
    result = evaluate_llm_modulo_pass(label, model, tokenizer, device, puzzles_by_theme, judge_engine)

    # free GPU memory (yippee!)
    del model
    torch.cuda.empty_cache()

    return {label: result}


